package com.wb.model.pojo.mobile.login;

/**
 * 登陆需要的信息
 * 
 * @author 郝洋
 * @version [版本号, 2016-4-12]
 * @see [相关类/方法]
 * @since [产品/模块版本]
 */
public class LoginVo
{
    private String totalCom;// 公司总数
    
    private String enterCom;// 新进公司数目
    
    private String outCom;// 流出公司数目
    
    private int prePayNum;// 预付费客户数目
    
    private int arrPayNum;// 欠费客户数目
    
    /**
     * 获取 totalCom
     * 
     * @return 返回 totalCom
     */
    public String getTotalCom()
    {
        return totalCom;
    }
    
    /**
     * 设置 totalCom
     * 
     * @param 对totalCom进行赋值
     */
    public void setTotalCom(String totalCom)
    {
        this.totalCom = totalCom;
    }
    
    /**
     * 获取 enterCom
     * 
     * @return 返回 enterCom
     */
    public String getEnterCom()
    {
        return enterCom;
    }
    
    /**
     * 设置 enterCom
     * 
     * @param 对enterCom进行赋值
     */
    public void setEnterCom(String enterCom)
    {
        this.enterCom = enterCom;
    }
    
    /**
     * 获取 outCom
     * 
     * @return 返回 outCom
     */
    public String getOutCom()
    {
        return outCom;
    }
    
    /**
     * 设置 outCom
     * 
     * @param 对outCom进行赋值
     */
    public void setOutCom(String outCom)
    {
        this.outCom = outCom;
    }
    
    public int getPrePayNum()
    {
        return prePayNum;
    }

    public void setPrePayNum(int prePayNum)
    {
        this.prePayNum = prePayNum;
    }

    public int getArrPayNum()
    {
        return arrPayNum;
    }

    public void setArrPayNum(int arrPayNum)
    {
        this.arrPayNum = arrPayNum;
    }
    
}
