package com.wb.model.pojo.computer;

public class CustomFollowInfo implements java.io.Serializable
{
    private String id;
    
    private String cusId;
    
    private String emId;
    
    private String followTime;
    
    private String content;
    
    private String contacts;
    
    private String tel;
    
    private String isRead;
    
    private String createTime;
    
    public CustomFollowInfo()
    {
    }
    
    public CustomFollowInfo(String id)
    {
        this.id = id;
    }
    
    public CustomFollowInfo(String id, String cusId, String emId, String followTime, String content,
        String contacts, String tel, String isRead, String createTime)
    {
        this.id = id;
        this.cusId = cusId;
        this.emId = emId;
        this.followTime = followTime;
        this.content = content;
        this.contacts = contacts;
        this.tel = tel;
        this.isRead = isRead;
        this.createTime = createTime;
    }
    
    public String getId()
    {
        return this.id;
    }
    
    public void setId(String id)
    {
        this.id = id;
    }
    
    public String getCusId()
    {
        return this.cusId;
    }
    
    public void setCusId(String cusId)
    {
        this.cusId = cusId;
    }
    
    public String getEmId()
    {
        return this.emId;
    }
    
    public void setEmId(String emId)
    {
        this.emId = emId;
    }
    
    public String getFollowTime()
    {
        return this.followTime;
    }
    
    public void setFollowTime(String followTime)
    {
        this.followTime = followTime;
    }
    
    public String getContent()
    {
        return this.content;
    }
    
    public void setContent(String content)
    {
        this.content = content;
    }
    
    public String getContacts()
    {
        return this.contacts;
    }
    
    public void setContacts(String contacts)
    {
        this.contacts = contacts;
    }
    
    public String getTel()
    {
        return this.tel;
    }
    
    public void setTel(String tel)
    {
        this.tel = tel;
    }
    
    public String getIsRead()
    {
        return this.isRead;
    }
    
    public void setIsRead(String isRead)
    {
        this.isRead = isRead;
    }
    
    public String getCreateTime()
    {
        return this.createTime;
    }
    
    public void setCreateTime(String createTime)
    {
        this.createTime = createTime;
    }
    
}